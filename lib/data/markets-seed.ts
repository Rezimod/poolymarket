function cat(slug: string): Category {
  return SEED_CATEGORIES.find((c) => c.slug === slug)!;
}

type MarketSeed = {
  categorySlug: string;
  id: string;
  title: string;
  title_ka: string;
  description: string;
  resolution_criteria: string;
  yes_price: number;
  no_price: number;
  total_volume: number;
  liquidity: number;
  end_date: string;
  is_featured: boolean;
  created_at: string;
  image_url?: string;
  resolution_source?: string | null;
};

function market(partial: MarketSeed): Market {
  const { categorySlug, image_url, resolution_source, ...rest } = partial;
  const category = cat(categorySlug);
  const title_ka = MARKET_TITLES_KA[rest.id] ?? rest.title_ka;
  return {
    ...rest,
    title_ka,
    category_id: category.id,
    category,
    creator_id: null,
    status: 'open',
    outcome: null,
    resolved_at: null,
    resolution_source: resolution_source ?? null,
    image_url: image_url ?? null,
  };
}

